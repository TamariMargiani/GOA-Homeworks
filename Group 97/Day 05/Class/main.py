# Boolean - A data type with two possible values: True or False

# A logical operation - takes several Boolean inputs and produces only 1 boolean output.

# The "and" operation results in a True value only when all the inputs are True at the same time. True and True = True / Every other combination = False

# The "or" logical operation results in a True value if at least one of the inputs is True.

# Logical operations need to be inserted in print() instructions for the result to be outputted.

# The result of a comparison operation is always a Boolean.

# You can store the result of a comparison operation as a variable. 

# Python is a case-sensitive language. 

 

